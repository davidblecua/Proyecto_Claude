const LANG_STORAGE_KEY = 'rentamaq_lang';
const DEFAULT_LANG = 'es';
const VALID_LANGS = ['es', 'ca', 'en'];

/**
 * Obtiene el idioma activo (localStorage → fallback 'es')
 */
function getLang() {
    const stored = localStorage.getItem(LANG_STORAGE_KEY);
    return VALID_LANGS.includes(stored) ? stored : DEFAULT_LANG;
}

/**
 * Guarda el idioma y actualiza la UI
 */
function setLang(lang) {
    if (!VALID_LANGS.includes(lang)) return;
    localStorage.setItem(LANG_STORAGE_KEY, lang);
    applyTranslations();
    updateLangSelector();
}

/**
 * Traduce una clave. Devuelve la clave si no existe.
 */
function t(key) {
    const lang = getLang();
    return (TRANSLATIONS[lang] && TRANSLATIONS[lang][key]) || TRANSLATIONS[DEFAULT_LANG][key] || key;
}

/**
 * Aplica traducciones a todos los elementos con data-i18n en el DOM.
 */
function applyTranslations() {
    document.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.getAttribute('data-i18n');
        const val = t(key);
        if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
            el.placeholder = val;
        } else {
            el.textContent = val;
        }
    });
    document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
        el.placeholder = t(el.getAttribute('data-i18n-placeholder'));
    });
}

/**
 * Actualiza el selector de idioma (bold/underline en activo)
 */
function updateLangSelector() {
    const current = getLang();
    document.querySelectorAll('.lang-btn').forEach(btn => {
        const isActive = btn.getAttribute('data-lang') === current;
        btn.style.fontWeight = isActive ? '700' : '400';
        btn.style.textDecoration = isActive ? 'underline' : 'none';
        btn.setAttribute('aria-pressed', isActive ? 'true' : 'false');
    });
}

// Aplicar idioma en carga
document.addEventListener('DOMContentLoaded', () => {
    applyTranslations();
    updateLangSelector();
});
